    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed

        switch (evt.getKeyCode()) {
            case KeyEvent.VK_W:
                mySnake.move("up");
                break;
            case KeyEvent.VK_S:
                mySnake.move("down");
                break;
            case KeyEvent.VK_A:
                mySnake.move("left");

                break;
            case KeyEvent.VK_D:
                mySnake.move("right");
                break;
            default:
                throw new AssertionError();
        }

    }//GEN-LAST:event_formKeyPressed

